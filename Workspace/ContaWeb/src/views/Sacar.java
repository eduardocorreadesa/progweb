	private void inputNomeFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_inputNomeFocusGained
		inputNome.setText(Util.NOME);
		String saldo = null;
		try {            
			saldo = OperacaoFinanceira.getSaldoPessoaJuridicaPessoaFisica(Util.CPFCNPJ);
		} catch (UnknownHostException ex) {
			Logger.getLogger(Sacar.class.getName()).log(Level.SEVERE, null, ex);
		}
		inputSaldo.setText(saldo);

	}//GEN-LAST:event_inputNomeFocusGained

