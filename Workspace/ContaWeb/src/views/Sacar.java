	private void btnSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacarActionPerformed

		OperacaoFinanceira.debitarSaldo(Util.CPFCNPJ, inputSacar.getText().toString());
		inputSacar.setText("");
		try {
			inputSaldo.setText(OperacaoFinanceira.getSaldoPessoaJuridicaPessoaFisica(Util.CPFCNPJ).toString());
		} catch (UnknownHostException ex) {
			Logger.getLogger(Sacar.class.getName()).log(Level.SEVERE, null, ex);
		}

	}//GEN-LAST:event_btnSacarActionPerformed

